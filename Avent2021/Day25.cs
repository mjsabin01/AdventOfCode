        public void Part1(string[] lines)
        {
            var rows = lines.Length;
            var cols = lines[0].Length;
            var current = new char[rows, cols];


            for (int row = 0; row < rows; row++)
            {
                for (int col = 0; col < cols; col++)
                {
                    current[row, col] = lines[row][col];
                }
            }


            var numTurns = 0;
            var performedMove = true;
            while (performedMove)
            {
                var moveRightResult = MoveRight(current, rows, cols);
                performedMove = moveRightResult.performedMove;
                current = moveRightResult.next;

                var moveDownResult = MoveDown(current, rows, cols);
                performedMove |= moveDownResult.performedMove;
                current = moveDownResult.next;

                numTurns++;
            }

            Console.WriteLine("After {0} turns, there are no more moves.", numTurns);
        }

        (char[,] next, bool performedMove) MoveRight(char[,] current, int rows, int cols)
        {
            var next = new char[rows, cols];
            var performedMove = false;
            for (int row = 0; row < rows; row++)
            {
                for (int col = cols - 1; col >=0; col--)
                {
                    var checkCol = col == cols - 1 ? 0 : col + 1;
                    switch (current[row, col])
                    {
                        case 'v':
                            next[row, col] = 'v';
                            break;
                        case '>':
                            if (current[row, checkCol] == '.')
                            {
                                next[row, checkCol] = '>';
                                performedMove = true;
                            }
                            else
                            {
                                next[row, col] = '>';
                            }
                            break;
                    }
                }
            }

            for (int row = 0; row < rows; row++)
            {
                for (int col = cols - 1; col >= 0; col--)
                {
                    if (next[row, col] == default)
                    {
                        next[row, col] = '.';
                    }
                }
            }

            return (next, performedMove);
        }

        (char[,] next, bool performedMove) MoveDown(char[,] current, int rows, int cols)
        {
            var next = new char[rows, cols];
            var performedMove = false;
            for (int row = 0; row < rows; row++)
            {
                var checkRow = row == rows - 1 ? 0 : row + 1;
                for (int col = cols - 1; col >= 0; col--)
                {
                    
                    switch (current[row, col])
                    {
                        case '>':
                            next[row, col] = '>';
                            break;
                        case 'v':
                            if (current[checkRow, col] == '.')
                            {
                                next[checkRow, col] = 'v';
                                performedMove = true;
                            }
                            else
                            {
                                next[row, col] = 'v';
                            }
                            break;
                    }
                }
            }

            for (int row = 0; row < rows; row++)
            {
                for (int col = cols - 1; col >= 0; col--)
                {
                    if (next[row, col] == default)
                    {
                        next[row, col] = '.';
                    }
                }
            }

            return (next, performedMove);
        }

